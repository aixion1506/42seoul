#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	ft_putnbr(int nb)
{
	char	ch;
	long	lnb;

	lnb = nb;
	if (lnb < 0)
	{
		lnb *= (-1);
		ft_putchar('-');
	}
	ch = (lnb % 10) + '0';
	if (lnb < 10)
		ft_putchar(ch);
	else
	{
		lnb /= 10;
		ft_putnbr(lnb);
		ft_putchar(ch);
	}
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		ft_putstr("\n");
		ft_putnbr(par[i].size);
		ft_putstr("\n");
		ft_putstr(par[i].copy);
		ft_putstr("\n");
		i++;
	}
}
